public class exemplo {  

    /*Declaração dos métodos*/  
    public static int som(int num1, int num2) {
        return num1 + num2;
    }  
    public static int sub(int num1, int num2){
        return num1 - num2;
    }  
    public static int div(int num1,int num2){
        return num1 / num2;
    }  
    public static int mult(int num1, int num2){
        return num1 * num2;
    } 
}